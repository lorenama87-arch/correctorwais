def get_pe(sub, pd, edad):
    tabla_etaria = BAREMOS_TODO.get(edad, BAREMOS_TODO["20:0-24:11"])
    dic_sub = tabla_etaria.get(sub, [])
    for limit, pe in dic_sub:
        if pd <= limit: return pe
    return 19

def buscar_indice(tipo, suma):
    tabla = COMP_MAP[tipo]
    if suma in tabla: return tabla[suma]
    ref = sorted(tabla.keys())
    if suma < ref[0]: return tabla[ref[0]]
    if suma > ref[-1]: return tabla[ref[-1]]
    for i in range(len(ref)-1):
        if ref[i] < suma < ref[i+1]:
            ratio = (suma - ref[i]) / (ref[i+1] - ref[i])
            return int(tabla[ref[i]] + ratio * (tabla[ref[i+1]] - tabla[ref[i]]))
    return 100

def desc_clinico(val):
    if val >= 130: return "Muy Superior"
    if val >= 120: return "Superior"
    if val >= 110: return "Sobre el Promedio"
    if val >= 90: return "Promedio"
    if val >= 80: return "Bajo el Promedio"
    if val >= 70: return "Limítrofe"
    return "Extremadamente Bajo"

